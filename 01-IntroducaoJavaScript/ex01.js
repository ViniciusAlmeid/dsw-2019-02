// Dobro
function dobro() {
	var n = parseInt(document.getElementById("numero").value);
	
	document.getElementById("saida").innerHTML = 
		"Dobro: " + (2 * n);
}


// Triplo
function triplo() {
	var n = parseInt(document.getElementById("numero").value);
	
	document.getElementById("saida").innerHTML = 
		"Triplo: " + (3 * n);
}

